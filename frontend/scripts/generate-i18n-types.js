#!/usr/bin/env node

/**
 * TypeScript Type Generation Script for i18n System
 *
 * This script automatically generates TypeScript types from translation files
 * to ensure type safety when using translation keys.
 *
 * Features:
 * - Reads both zh-TW.json and en-US.json translation files
 * - Extracts all translation keys using dot notation for nested keys
 * - Generates a union type of all possible keys
 * - Outputs to frontend/types/i18n.generated.ts
 *
 * Usage:
 *   npm run generate:i18n-types
 *   or
 *   node frontend/scripts/generate-i18n-types.js
 *
 * @see Requirements 12.1
 */

const fs = require('fs');
const path = require('path');

// Configuration
const LOCALES_DIR = path.join(__dirname, '../locales');
const OUTPUT_FILE = path.join(__dirname, '../types/i18n.generated.ts');
const TRANSLATION_FILES = ['zh-TW.json', 'en-US.json'];

/**
 * Recursively traverse a translation object and build dot-notation keys
 *
 * @param obj - Translation object to traverse
 * @param prefix - Current key prefix (for nested keys)
 * @returns Array of dot-notation keys
 *
 * @example
 * extractKeys({ nav: { articles: "文章" } }, "")
 * // Returns: ["nav.articles"]
 */
function extractKeys(obj, prefix = '') {
  const keys = [];

  for (const key in obj) {
    if (!obj.hasOwnProperty(key)) continue;

    const fullKey = prefix ? `${prefix}.${key}` : key;
    const value = obj[key];

    if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
      // Recursively traverse nested objects
      keys.push(...extractKeys(value, fullKey));
    } else if (typeof value === 'string') {
      // Leaf node - add the key
      keys.push(fullKey);
    }
  }

  return keys;
}

/**
 * Read and parse a translation JSON file
 *
 * @param filePath - Path to the translation file
 * @returns Parsed translation object
 * @throws Error if file cannot be read or parsed
 */
function readTranslationFile(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    return JSON.parse(content);
  } catch (error) {
    throw new Error(`Failed to read translation file ${filePath}: ${error}`);
  }
}

/**
 * Generate TypeScript union type from translation keys
 *
 * @param keys - Array of translation keys
 * @returns TypeScript type definition string
 */
function generateTypeDefinition(keys) {
  // Sort keys alphabetically for consistent output
  const sortedKeys = [...new Set(keys)].sort();

  // Generate the type definition
  const typeLines = sortedKeys.map((key) => `  | '${key}'`);

  return `/**
 * Auto-generated translation keys
 *
 * This file is automatically generated from translation files.
 * DO NOT EDIT MANUALLY - run \`npm run generate:i18n-types\` to regenerate.
 *
 * @generated
 */

/**
 * Union type of all available translation keys
 *
 * Use this type to ensure type safety when calling the translation function.
 *
 * @example
 * const key: TranslationKey = 'nav.articles'; // ✓ Valid
 * const key: TranslationKey = 'invalid.key'; // ✗ Type error
 */
export type TranslationKey =
${typeLines.join('\n')};
`;
}

/**
 * Main execution function
 */
function main() {
  console.log('🔧 Generating i18n TypeScript types...\n');

  // Collect all keys from all translation files
  const allKeys = [];
  const keysByFile = {};

  for (const fileName of TRANSLATION_FILES) {
    const filePath = path.join(LOCALES_DIR, fileName);

    console.log(`📖 Reading ${fileName}...`);

    if (!fs.existsSync(filePath)) {
      console.error(`❌ Error: Translation file not found: ${filePath}`);
      process.exit(1);
    }

    const translations = readTranslationFile(filePath);
    const keys = extractKeys(translations);

    keysByFile[fileName] = keys;
    allKeys.push(...keys);

    console.log(`   Found ${keys.length} keys`);
  }

  // Verify key consistency across files
  console.log('\n🔍 Verifying key consistency...');

  const uniqueKeys = new Set(allKeys);
  const [file1, file2] = TRANSLATION_FILES;
  const keys1 = new Set(keysByFile[file1]);
  const keys2 = new Set(keysByFile[file2]);

  const missingInFile2 = [...keys1].filter((key) => !keys2.has(key));
  const missingInFile1 = [...keys2].filter((key) => !keys1.has(key));

  if (missingInFile2.length > 0) {
    console.warn(`⚠️  Warning: Keys in ${file1} but missing in ${file2}:`);
    missingInFile2.forEach((key) => console.warn(`   - ${key}`));
  }

  if (missingInFile1.length > 0) {
    console.warn(`⚠️  Warning: Keys in ${file2} but missing in ${file1}:`);
    missingInFile1.forEach((key) => console.warn(`   - ${key}`));
  }

  if (missingInFile1.length === 0 && missingInFile2.length === 0) {
    console.log('✅ All keys are consistent across translation files');
  }

  // Generate type definition
  console.log('\n📝 Generating type definition...');
  const typeDefinition = generateTypeDefinition([...uniqueKeys]);

  // Write to output file
  console.log(`💾 Writing to ${path.relative(process.cwd(), OUTPUT_FILE)}...`);
  fs.writeFileSync(OUTPUT_FILE, typeDefinition, 'utf-8');

  console.log(`\n✨ Successfully generated ${uniqueKeys.size} translation key types!`);
  console.log(`📄 Output: ${path.relative(process.cwd(), OUTPUT_FILE)}`);
}

// Execute main function
if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error('\n❌ Error generating types:', error);
    process.exit(1);
  }
}
